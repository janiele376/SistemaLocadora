    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
String codigo = jTF_Codigo.getText();
        String nome = jTF_Nome.getText();
        String cpf = jTF_CPF.getText();
        String telefone = jTF_Telefone.getText();
        String numero = jTF_Numero.getText();
        
        // Verifica campos obrigatórios
        if (codigo.equals("") || nome.equals("") || cpf.equals("")) {
            JOptionPane.showMessageDialog(null, "Pesquise um cliente e preencha os campos!", 
                    "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            Connection con = Conexao.AbrirConexao();
            ClienteDAO sql = new ClienteDAO(con);
            Cliente a = new Cliente();
            
            try {
                // Preenche o ID (que não muda)
                a.setCodigo(Integer.parseInt(codigo));
                
                // Preenche os outros dados
                a.setNome(nome);
                a.setRg(jTF_RG.getText());
                a.setCpf(cpf);
                a.setTelefone(telefone);
                a.setEmail(jTF_Email.getText());
                a.setRua(jTF_Rua.getText());
                a.setBairro(jTF_Bairro.getText());
                a.setCep(jTF_CEP.getText());
                
                // Conversão do Número da casa
                a.setNumero(Integer.parseInt(numero));
                
                // Manda alterar
                String resultado = sql.Alterar_Cliente(a);
                
                JOptionPane.showMessageDialog(null, resultado);
                
                // Limpa tudo se deu certo
                if (resultado.contains("Sucesso")) {
                    jTF_Codigo.setText("");
                    LimparCampos();
                }
                
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Verifique se o Código e o Número são válidos!", 
                        "Erro", JOptionPane.ERROR_MESSAGE);
            } finally {
                Conexao.FecharConexao(con);
            }
        }
              // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
String codigo = jTF_CodigoPesquisa.getText();
        
        if (codigo.equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o código do Cliente!", 
                    "Vídeo Locadora", JOptionPane.WARNING_MESSAGE);
        } else {
            Connection con = Conexao.AbrirConexao();
            ClienteDAO sql = new ClienteDAO(con);
            
            try {
                int cod = Integer.parseInt(codigo);
                
                // Busca o cliente pelo código
                List<Cliente> lista = sql.Pesquisar_Cod_Cliente(cod);
                
                if (lista.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Cliente não encontrado!", 
                            "Erro", JOptionPane.ERROR_MESSAGE);
                    // Limpa os campos se não achar
                    LimparCampos();
                } else {
                    // Pega o primeiro cliente da lista (o único encontrado)
                    Cliente a = lista.get(0);
                    
                    // Preenche todos os campos na tela
                    jTF_Nome.setText(a.getNome());
                    jTF_RG.setText(a.getRg());
                    jTF_CPF.setText(a.getCpf());
                    jTF_RG.setText(a.getTelefone());
                    jTF_Email.setText(a.getEmail());
                    jTF_Rua.setText(a.getRua());
                    jTF_Numero.setText(String.valueOf(a.getNumero())); // Converte int para String
                    jTF_Bairro.setText(a.getBairro());
                    jTF_CEP.setText(a.getCep());
                }
                
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "O Código deve ser um número inteiro!", 
                        "Erro", JOptionPane.ERROR_MESSAGE);
            } finally {
                Conexao.FecharConexao(con);
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

